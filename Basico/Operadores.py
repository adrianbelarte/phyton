# Operadores

print(3 + 5)
print(5 - 2)
print(3 * 2)
print(6 / 2)
print(15 % 3) # modulo
print(10 // 3) # flor division
print(2 ** 3) # calcular exponente

myFloat = 2.5 * 2
print("Hola " * int(myFloat))

# Comparador aridmetico

print(3 > 4)  # mayor que..
print(3 < 4)  # menor que..
print(3 >= 4) # mayor o igual..
print(3 <= 4) # menor o igual..
print(3 == 4) # igual que..
print(3 != 4) # diferente de..

print("hola" > "python") # Ordenación alfabética
print(len("aaaa") >= len("abaa")) # Cuenta caracteres

# Operadores Lógicos

print(3 > 4 and "hola" > "python")
print(3 > 4 or "hola" > "python")
print(not(3 > 4))




